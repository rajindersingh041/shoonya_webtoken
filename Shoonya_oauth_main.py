def batch_subscribe_loop():
    global pending_subscriptions, subscribed_tokens

    while True:
        if pending_subscriptions:
            tokens = list(pending_subscriptions)
            pending_subscriptions.clear()

            for i in range(0, len(tokens), CHUNK_SIZE):
                batch = tokens[i : i + CHUNK_SIZE]

                try:
                    api.subscribe(batch, feed_type="d")
                    subscribed_tokens.update(batch)
                    # print(f"📡 Subscribed {len(batch)} tokens")
                except Exception as e:
                    print("Batch subscribe error:", e)

                time.sleep(SUB_DELAY)

        time.sleep(0.1)
    print("Batch subscribed")


